     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'courseName' => 'required'
        ]);

        $subject = new Subject;
        $subject->courseName = $request->input('courseName');
        $subject->user_id = auth()->user()->id;

        $subject->save();

        return redirect('/home');
    }

